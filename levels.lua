function levels:draw()
	local lg = love.graphics

	for i, b in pairs(self.bricks_table) do
		lg.draw(self.img_brick, b.img, b.x, b.y)
	end

	lg.print(self.brick_no, 400)
end

function levels:addBrick()
	local lg = love.graphics

	for i, l in pairs(self.levels) do
		if i == self.lvl_no then
			for y = 1, self.map_height do
				for x = 1, self.map_width do
					if self.current_lvl[y][x] == 1 then
						self.brick_no = self.brick_no + 1
						local brick = {}
							  brick.x = (x - 1) * self.tile_width
							  brick.y = (y - 1) * self.tile_height
							  brick.width = self.tile_width
							  brick.height = self.tile_height
						  	  brick.lvl = math.random(1, 3)
						 	  brick.img = self.brick_lvls[brick.lvl]
						 	  brick.powerup = math.random(0, 5)

						table.insert(self.bricks_table, brick)
					end
				end
			end
		end
	end
end

function levels:change()
	if self.brick_no <= 0 then
		if self.lvl_no == 10 then
			ball.game_end = true
			self.lvl_no = 1
		end
		if self.lvl_no < 10 then
			self.lvl_no = self.lvl_no + 1
		end
		ball.start = false
		self.current_lvl = self.levels[self.lvl_no]
		levels:addBrick()
		paddle.bullets_left = 15
		paddle.x = (screenWidth - paddle.width) / 2
		paddle.y = 580
		local random = math.random(1, 3)
		self.current_background = self.backgrounds[random]
	end
end